# Lets make a simple Calculator 

a = 33
b = 4

add = a+b
print("Addition of",a," + ",b," = ",add)

sub = a-b
print("Subtraction of",a," - ",b," = ",sub)

multi = a*b
print("Multiplication of ",a," * ",b," = ",multi)

div = a/b 
print("Division of ", a,  "/",b," = ",div)

mod = a % b
print("Modulus of ",a," % ",b, " = ",mod)

f_div = a//b
print("Floor division of ",a," // ",b," = ",f_div)

exponential = a ** b
print("Exponential of ",a," ** ",b," = ",exponential)
